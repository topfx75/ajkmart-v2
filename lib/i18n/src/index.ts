export function t(key: TranslationKey, lang: Language): string {
  const base = translations[lang] as Record<string, string>;
  const en = translations.en as Record<string, string>;
  return base[key] ?? en[key] ?? String(key);
}

export function tDual(key: TranslationKey, lang: Language): string {
  if (lang === "en_roman") {
    const en = translations.en[key] ?? String(key);
    const roman = translations.roman[key] ?? String(key);
    if (en === roman) return en;
    return `${en}\n${roman}`;
  }
  if (lang === "en_ur") {
    const en = translations.en[key] ?? String(key);
    const ur = translations.ur[key] ?? String(key);
    if (en === ur) return en;
    return `${en}\n${ur}`;
  }
  return t(key, lang);
}

export function isRTL(lang: Language): boolean {
  return lang === "ur";
}

export function getDir(lang: Language): "ltr" | "rtl" {
  return isRTL(lang) ? "rtl" : "ltr";
}
